// domainNeedsAuth returns true if any func in the domain uses authz or currentUser.
func domainNeedsAuth(funcs []ssacparser.ServiceFunc, domain string) bool {
	for _, fn := range funcs {
		if fn.Domain != domain {
			continue
		}
		for _, seq := range fn.Sequences {
			if seq.Type == "auth" {
				return true
			}
			// Check if any arg references currentUser.
			for _, arg := range seq.Args {
				if arg.Source == "currentUser" {
					return true
				}
			}
		}
	}
	return false
}

// lcFirst converts to camelCase (lowercases the first character with Go initialism handling).
func lcFirst(s string) string {
	return strcase.ToGoCamel(s)
}

// resolveSuccessStatus finds the 2xx success response code for an operationId in OpenAPI
// and returns the corresponding Go http.Status constant. Returns "" if not found.
func resolveSuccessStatus(doc *openapi3.T, operationID string) string {
	if doc == nil || doc.Paths == nil {
		return ""
	}
	for _, pi := range doc.Paths.Map() {
		for _, op := range pi.Operations() {
			if op.OperationID != operationID {
				continue
			}
			if op.Responses == nil {
				return ""
			}
			for code := range op.Responses.Map() {
				if len(code) == 3 && code[0] == '2' {
					return httpStatusConst(code)
				}
			}
			return ""
		}
	}
	return ""
}

// httpStatusConst converts a numeric HTTP status code string to Go's net/http constant name.
func httpStatusConst(code string) string {
	switch code {
	case "200":
		return "http.StatusOK"
	case "201":
		return "http.StatusCreated"
	case "204":
		return "http.StatusNoContent"
	default:
		return "http.StatusOK"
	}
}

// buildFileToOperationID builds a map from generated .go filename to operationID.
// SSaC parser sets FileName to the .ssac basename (e.g. "create_gig.ssac"),
// so we convert to .go extension for lookup by the generated file.
func buildFileToOperationID(funcs []ssacparser.ServiceFunc) map[string]string {
	result := make(map[string]string)
	for _, fn := range funcs {
		key := strings.TrimSuffix(fn.FileName, ".ssac") + ".go"
		result[key] = fn.Name
	}
	return result
}

// collectModelIncludes extracts per-model x-include specs from OpenAPI operations.
// Maps operationId → model via serviceFuncs, then merges all include specs per model.
// Returns map[ModelName][]string where values are raw include specs (e.g. "user", "instructor_id:user").
func collectModelIncludes(doc *openapi3.T, funcs []ssacparser.ServiceFunc) map[string][]string {
	// Map operationId → model name (from the first get sequence with @model).
	opToModel := make(map[string]string)
	for _, fn := range funcs {
		for _, seq := range fn.Sequences {
			if seq.Model != "" && seq.Type == "get" {
				parts := strings.SplitN(seq.Model, ".", 2)
				if len(parts) >= 1 {
					opToModel[fn.Name] = parts[0]
					break
				}
			}
		}
	}

	result := make(map[string][]string)
	if doc == nil || doc.Paths == nil {
		return result
	}

	for _, pathItem := range doc.Paths.Map() {
		for _, op := range pathItem.Operations() {
			if op.OperationID == "" {
				continue
			}
			incCfg := getExtMap(op, "x-include")
			if incCfg == nil {
				continue
			}
			allowed := getStrSlice(incCfg, "allowed")
			if len(allowed) == 0 {
				continue
			}
			modelName, ok := opToModel[op.OperationID]
			if !ok {
				continue
			}
			existing := result[modelName]
			for _, spec := range allowed {
				found := false
				for _, e := range existing {
					if e == spec {
						found = true
						break
					}
				}
				if !found {
					existing = append(existing, spec)
				}
			}
			result[modelName] = existing
		}
	}

	return result
}

// collectCursorSpecs extracts cursor column Go field name per operationId.
// Returns map[operationId]string ("ID" default, or PascalCase of x-sort.default).
func collectCursorSpecs(doc *openapi3.T) map[string]string {
	result := make(map[string]string)
	if doc == nil || doc.Paths == nil {
		return result
	}
	for _, pi := range doc.Paths.Map() {
		for _, op := range pi.Operations() {
			if op == nil || op.OperationID == "" {
				continue
			}
			pag := getExtMap(op, "x-pagination")
			if pag == nil || getStr(pag, "style", "") != "cursor" {
				continue
			}
			cursorField := "ID"
			if sortExt := getExtMap(op, "x-sort"); sortExt != nil {
				if def := getStr(sortExt, "default", ""); def != "" {
					cursorField = strcase.ToGoPascal(def)
				}
			}
			result[op.OperationID] = cursorField
		}
	}
	return result
}
