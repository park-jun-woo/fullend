//ff:func feature=genmodel type=generator control=iteration dimension=1
//ff:what TestGenerateEscrow: GenerateBytes로 escrow OpenAPI에서 인터페이스·메서드·생성자 코드 생성 검증
package genmodel

import (
	"os"
	"strings"
	"testing"
)

func TestGenerateEscrow(t *testing.T) {
	data, err := os.ReadFile("testdata/escrow.openapi.yaml")
	if err != nil {
		t.Fatal(err)
	}

	code, err := GenerateBytes("escrow.openapi.yaml", data)
	if err != nil {
		t.Fatal(err)
	}
	src := string(code)

	// Interface
	if !strings.Contains(src, "type EscrowModel interface {") {
		t.Error("missing EscrowModel interface")
	}

	// Methods
	for _, method := range []string{"Hold(", "Release(", "GetStatus(", "Cancel("} {
		if !strings.Contains(src, method) {
			t.Errorf("missing method %s", method)
		}
	}

	// Response types
	for _, typ := range []string{"HoldResponse", "ReleaseResponse", "GetStatusResponse"} {
		if !strings.Contains(src, "type "+typ+" struct {") {
			t.Errorf("missing response type %s", typ)
		}
	}

	// Cancel has no 200 response, should return error only
	if strings.Contains(src, "CancelResponse") {
		t.Error("Cancel should not have a response type (204 only)")
	}

	// Constructor
	if !strings.Contains(src, "func NewEscrowModel(baseURL string) EscrowModel {") {
		t.Error("missing constructor")
	}

	// Client struct
	if !strings.Contains(src, "escrowClient struct {") {
		t.Error("missing client struct")
	}

	// do() helper
	if !strings.Contains(src, "func (c *escrowClient) do(") {
		t.Error("missing do() helper")
	}

	// Path parameter in GetStatus
	if !strings.Contains(src, "fmt.Sprintf(\"/escrow/%v\"") {
		t.Error("missing path parameter formatting in GetStatus")
	}

	// Generated header
	if !strings.Contains(src, "Code generated by fullend gen-model. DO NOT EDIT.") {
		t.Error("missing generated header")
	}

	// context.Context parameter
	if !strings.Contains(src, "ctx context.Context") {
		t.Error("missing context.Context parameter")
	}
}
