//ff:func feature=genmodel type=generator control=iteration dimension=1
//ff:what 서비스명과 OpenAPI 문서로부터 Go 클라이언트 코드를 생성한다
package genmodel

import (
	"bytes"
	"fmt"
	"go/format"

	"github.com/getkin/kin-openapi/openapi3"
)

func generateCode(serviceName string, doc *openapi3.T) ([]byte, error) {
	var buf bytes.Buffer

	methods := extractMethods(doc)
	types := extractResponseTypes(serviceName, methods, doc)
	needsTime := typesNeedTime(types)

	// Header
	buf.WriteString("// Code generated by fullend gen-model. DO NOT EDIT.\n")
	buf.WriteString("package external\n\n")

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"bytes\"\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"encoding/json\"\n")
	buf.WriteString("\t\"fmt\"\n")
	buf.WriteString("\t\"net/http\"\n")
	if needsTime {
		buf.WriteString("\t\"time\"\n")
	}
	buf.WriteString(")\n\n")

	// Interface
	fmt.Fprintf(&buf, "type %sModel interface {\n", serviceName)
	for _, m := range methods {
		fmt.Fprintf(&buf, "\t%s\n", m.signature())
	}
	buf.WriteString("}\n\n")

	// Response types
	for _, t := range types {
		fmt.Fprintf(&buf, "type %s struct {\n", t.Name)
		for _, f := range t.Fields {
			fmt.Fprintf(&buf, "\t%s %s `json:\"%s\"`\n", f.Name, f.GoType, f.JSONName)
		}
		buf.WriteString("}\n\n")
	}

	// Client struct
	lowerName := lcFirst(serviceName)
	fmt.Fprintf(&buf, "type %sClient struct {\n", lowerName)
	buf.WriteString("\tbaseURL string\n")
	buf.WriteString("\tclient  *http.Client\n")
	buf.WriteString("}\n\n")

	// Constructor
	fmt.Fprintf(&buf, "func New%sModel(baseURL string) %sModel {\n", serviceName, serviceName)
	fmt.Fprintf(&buf, "\treturn &%sClient{baseURL: baseURL, client: &http.Client{}}\n", lowerName)
	buf.WriteString("}\n\n")

	// Method implementations
	for _, m := range methods {
		buf.WriteString(m.implementation(lowerName + "Client"))
		buf.WriteString("\n")
	}

	// do() helper
	buf.WriteString(doHelper(lowerName + "Client"))

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("gofmt: %w\n--- raw ---\n%s", err, buf.String())
	}
	return formatted, nil
}
